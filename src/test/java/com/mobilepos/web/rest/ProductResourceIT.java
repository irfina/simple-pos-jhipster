    @Test
    @Transactional
    void putExistingProduct() throws Exception {
        // Initialize the database
        productRepository.saveAndFlush(product);

        long databaseSizeBeforeUpdate = getRepositoryCount();

        // Update the product
        Product updatedProduct = productRepository.findById(product.getId()).orElseThrow();
        // Disconnect from session so that the updates on updatedProduct are not directly saved in db
        em.detach(updatedProduct);
        updatedProduct
            .sku(UPDATED_SKU)
            .name(UPDATED_NAME)
            .barcode(UPDATED_BARCODE)
            .discountInPercent(UPDATED_DISCOUNT_IN_PERCENT)
            .minDiscQty(UPDATED_MIN_DISC_QTY)
            .sellPrice(UPDATED_SELL_PRICE)
            .isVatApplied(UPDATED_IS_VAT_APPLIED)
            .isActive(UPDATED_IS_ACTIVE)
            .isStockable(UPDATED_IS_STOCKABLE)
            .notes(UPDATED_NOTES)
            .createdAt(UPDATED_CREATED_AT)
            .createdBy(UPDATED_CREATED_BY)
            .lastUpdatedAt(UPDATED_LAST_UPDATED_AT)
            .lastUpdatedBy(UPDATED_LAST_UPDATED_BY);
        ProductDTO productDTO = productMapper.toDto(updatedProduct);

        restProductMockMvc
            .perform(
                put(ENTITY_API_URL_ID, productDTO.getId()).contentType(MediaType.APPLICATION_JSON).content(om.writeValueAsBytes(productDTO))
            )
            .andExpect(status().isOk());

        // Validate the Product in the database
        assertSameRepositoryCount(databaseSizeBeforeUpdate);
        assertPersistedProductToMatchAllProperties(updatedProduct);
    }

